package io.deniffel.raytracer;

public class Tuple {

    public final double x;
    public final double y;
    public final double z;
    public final double w;

    public Tuple(double x, double y, double z, double w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}
